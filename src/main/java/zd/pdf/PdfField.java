package zd.pdf;

public class PdfField {
	
	public static class PdfFieldData{
	float left;
	float top;
	float width;
	float height;
	String name;
	
	public float getLeft() {
		return left;
	}
	public float getTop(){
		return top;
	}
	public float width() {
		return width;
	}
	public float height(){
		return height;
	}
	public String getName() {
		return name;
	}
	}
}
